/*
Operator Perbandingan
- Operator perbandingan adalah operator yang hasilnya adalah bool
- Operator perbandingan bisa digunakan di tipe data number dan string

=====================================================
|     Operator    |           Keterangan            |
|====================================================
|         ==      |          Sama dengan            |
|         !=      |       Tidak sama dengan         |
|         >       |           Lebih dari            |
|         <       |           Kurang dari           |
|        >=       |    Lebih dari atau sama dengan  |
|        <=       |   Kurang dari atau sama dengan  |
=====================================================
*/

void main(){
  print("==dengan intiger===");
  print(10 == 10);
  print(10 != 10);
  print(10 > 10);
  print(10 < 10);
  print(10 >= 10);
  print(10 <= 10);

  print("==dengan string==");
  print('bima' == 'bima');
  print('bima' != 'bima');
}
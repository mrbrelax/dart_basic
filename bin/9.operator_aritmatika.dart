
/*
Operator Aritmatika
|======================================================
|     Operator          |       Keterangan            |
|======================================================
|         +             |   Pertambahan               |
|     -, -expression    |   Pengurangan atau Negatif  |
|         *             |   Perkalian                 |
|         /             |   Pembagian, Hasil double   |
|        ~/             |   Pembagian, Hasin Int      |
|         %             |   Sisa Bagi                 |
=======================================================

*/
void main(){
  var first = 20;
  var second = 10;
  
  print(first + second);
  print(first - second);
  print(first * second);
  print(first / second);
  print(first ~/ second);
  print(first % second);
}
/*
Number
  - Number adalah tipe data angka, terdapat dua jenis tipe data number yaitu int dan double
  - int adalah data bilangan bulat
  - double adalah tipe data bilangan desimal
  - penulisan koma dalam double menggunakan titik, bukan koma, jadi kita akan membuat bilangan desimal 0,5 (nol koma lima),
  maka ditulis 0,5

Tipe data Num
- jika kita ingin menggunakan tipe data number yang bisa int ataupun double, kita bisa menggunakan tipe data num  
*/

void main(){
  int number1 = 10;
  double number2 = 10.5;
  print("===Belajar tipe data int dan double===");
  print(number1);
  print(number2);
  
  print("===Belajar tipe data num===");
  num angka = 20;
  print(angka);
  angka = 20.5;
  print(angka);
}